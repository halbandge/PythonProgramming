a = 10 # integer variable
print(a)
print(type(a))

# we can represent integer variable in 4 different formats
# 1. decimal form (base 10)
# 2. binary form (base 2)
# 3. octal form (base 8)
# 4. hexadecimal form (base 16)

decimal_form = 123
print(decimal_form) # 123
print(type(decimal_form))

# binary form -> using prefix 0b or 0B
binary_form = 0b1111
print(binary_form) # 15
print(type(binary_form))

# octal form -> using prefix 0o or 0O
octal_form = 0o123
print(octal_form) # 83
print(type(octal_form))

# hexadecimal form -> using prefix 0x or 0X
hexadecimal_form = 0x10
print(hexadecimal_form)
print(type(hexadecimal_form))