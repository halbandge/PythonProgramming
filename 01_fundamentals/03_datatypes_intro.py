# integer datatype
a = 10
print(a) # this will print value 10 in the console
print(type(a)) # this will tell the type of variable a -> <class 'int'>

# float datatype
b = 10.5 
print(type(b)) # <class 'float'>

# boolean datatype
c = True
print(type(c)) # <class 'bool'>

